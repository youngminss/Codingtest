import sys

def print_gcf(a, b) :
    if a < b :
        a, b = b, a
    
    while b != 0 :
        a = a % b
        a, b = b, a 
    
    print('1' * a)

if __name__ == '__main__' :
    a, b = map(int, sys.stdin.readline().rstrip('\n').split())
    print_gcf(a, b)


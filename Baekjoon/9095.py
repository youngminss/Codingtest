from sys import stdin

def solution(n) :
    dp = [1, 1, 2]
    for i in range(3, n + 1) : 
        dp.append(dp[i-1] + dp[i-2] + dp[i-3])

    return dp[n]

if __name__ == '__main__' :
    T = int(stdin.readline().rstrip('\n'))
    for _ in range(T) : 
        print(solution(int(stdin.readline().rstrip('\n'))))

